package java.model;

public class Figure {

    private String x;
    private String y;
    private Boolean isCircle;

    public String getX() {
        return x;
    }
    public void setX( final String x ) {
        this.x = x;
    }
    public String getY() {
        return y;
    }
    public void setY( final String y ) {
        this.y = y;
    }
    public Boolean getCircle() {
        return isCircle;
    }
    public void setCircle( final Boolean circle ) {
        isCircle = circle;
    }
}
