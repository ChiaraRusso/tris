package java.model;

public class Figure {

    private Integer x;
    private Integer y;
    private Boolean isCircle;

    public Integer getX() {
        return x;
    }
    public void setX( final Integer x ) {
        this.x = x;
    }
    public Integer getY() {
        return y;
    }
    public void setY( final Integer y ) {
        this.y = y;
    }
    public Boolean getCircle() {
        return isCircle;
    }
    public void setCircle( final Boolean circle ) {
        isCircle = circle;
    }
}
